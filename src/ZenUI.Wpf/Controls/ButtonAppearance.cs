namespace ZenUI.Wpf.Controls
{
    /// <summary>
    /// 指定按钮的视觉呈现形式。
    /// </summary>
    public enum ButtonAppearance
    {
        /// <summary>
        /// 使用填充背景。
        /// </summary>
        Filled,

        /// <summary>
        /// 使用透明背景和可见边框。
        /// </summary>
        Outlined,

        /// <summary>
        /// 不显示背景和边框。
        /// </summary>
        Text
    }
}
