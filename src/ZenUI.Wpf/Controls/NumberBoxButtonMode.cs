namespace ZenUI.Wpf.Controls
{
    /// <summary>
    /// 指定 <see cref="ZenNumberBox"/> 的增减按钮布局。
    /// </summary>
    public enum NumberBoxButtonMode
    {
        /// <summary>
        /// 减少按钮位于输入框左侧，增加按钮位于输入框右侧。
        /// </summary>
        Horizontal,

        /// <summary>
        /// 增加和减少按钮垂直排列在输入框右侧。
        /// </summary>
        Vertical
    }
}
